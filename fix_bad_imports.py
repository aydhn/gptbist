# Fix existing files calling app.performance_app models
def fix_file(path, replacements):
    with open(path, "r") as f:
        content = f.read()

    modified = False
    for old, new in replacements.items():
        if old in content:
            content = content.replace(old, new)
            modified = True

    if modified:
        with open(path, "w") as f:
            f.write(content)

